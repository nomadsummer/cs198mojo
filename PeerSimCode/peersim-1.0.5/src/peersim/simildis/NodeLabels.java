package peersim.simildis;

public class NodeLabels  {
	public float similarity;
	public byte level;
	
	public NodeLabels(float initSimilarity , byte initLevel){
		this.similarity = initSimilarity;
		this.level = initLevel;
	}
	
	public String toString(){
		return "("+this.similarity+","+this.level+")";
	}

}
